// ABOUTME: Prisma client instance for database operations in the ProfiCo Inventory Management System
// ABOUTME: Provides a singleton database connection with proper edge runtime handling

import { PrismaClient } from "@prisma/client";

const globalForPrisma = globalThis as unknown as {
  prisma: PrismaClient | undefined;
};

export const prisma =
  globalForPrisma.prisma ??
  new PrismaClient({
    log: ["query"],
  });

if (process.env.NODE_ENV !== "production") globalForPrisma.prisma = prisma;
